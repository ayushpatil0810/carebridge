/**
 * Get message text for a template in a specific language
 * Falls back to English if the language is not available
 */
export function getTemplateMessage(template, lang = "en") {
  if (!template) return "";
  if (template.messages && template.messages[lang]) {
    return template.messages[lang];
  }
  if (template.messages && template.messages.en) {
    return template.messages.en;
  }
  return template.message || "";
}

/**
 * Get localized title for a template
 */
export function getTemplateTitle(template, lang = "en") {
  if (!template) return "";
  if (template.titles && template.titles[lang]) {
    return template.titles[lang];
  }
  return template.title || "";
}

